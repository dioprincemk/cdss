class OpenAIProvider(LLMProvider):
    """OpenAI GPT-based clinical explanation provider."""

    async def generate_explanation(self, prompt_data: Dict) -> Dict:
        prompt = CLINICAL_PROMPT_TEMPLATE.format(**prompt_data)
        headers = {
            "Authorization": f"Bearer {settings.OPENAI_API_KEY}",
            "Content-Type": "application/json",
        }
        payload = {
            "model": settings.OPENAI_MODEL,
            "messages": [
                {"role": "system", "content": "You are a clinical decision support AI. Always respond in valid JSON."},
                {"role": "user", "content": prompt},
            ],
            "max_tokens": 1000,
            "temperature": 0.3,
            "response_format": {"type": "json_object"},
        }
        async with httpx.AsyncClient(timeout=30) as client:
            response = await client.post(
                "https://api.openai.com/v1/chat/completions",
                headers=headers,
                json=payload,
            )
            response.raise_for_status()
            data = response.json()

        content = data["choices"][0]["message"]["content"]
        parsed = json.loads(content)
        tokens_used = data.get("usage", {}).get("total_tokens")

        return {
            "explanation": parsed.get("explanation", ""),
            "severity": parsed.get("severity", "Moderate"),
            "clinical_reasoning": parsed.get("clinical_reasoning", ""),
            "recommendations": parsed.get("recommendations", []),
            "provider": "openai",
            "model_name": settings.OPENAI_MODEL,
            "tokens_used": tokens_used,
            "raw_response": data,
        }


class LocalLLMProvider(LLMProvider):
    """OpenAI-compatible local LLM provider (Ollama, LM Studio, etc.)."""

    async def generate_explanation(self, prompt_data: Dict) -> Dict:
        prompt = CLINICAL_PROMPT_TEMPLATE.format(**prompt_data)
        payload = {
            "model": "llama3.2",
            "messages": [
                {"role": "user", "content": prompt}
            ],
            "stream": False,
        }
        async with httpx.AsyncClient(timeout=60) as client:
            response = await client.post(
                f"{settings.LOCAL_LLM_URL}/api/chat",
                json=payload,
            )
            response.raise_for_status()
            data = response.json()

        content = data.get("message", {}).get("content", "{}")
        # Strip markdown code fences if present
        if "```json" in content:
            content = content.split("```json")[1].split("```")[0].strip()
        parsed = json.loads(content)

        return {
            "explanation": parsed.get("explanation", ""),
            "severity": parsed.get("severity", "Moderate"),
            "clinical_reasoning": parsed.get("clinical_reasoning", ""),
            "recommendations": parsed.get("recommendations", []),
            "provider": "local",
            "model_name": "local-llm",
            "tokens_used": None,
            "raw_response": data,
        }


def get_llm_provider() -> LLMProvider:
    """
    Factory function: returns the configured LLM provider.
    Change LLM_PROVIDER in .env to switch providers.
    """
    provider_map = {
        "openai": OpenAIProvider,
        "local": LocalLLMProvider,
        "mock": MockProvider,
    }
    cls = provider_map.get(settings.LLM_PROVIDER, MockProvider)
    logger.info(f"Using LLM provider: {settings.LLM_PROVIDER} ({cls.__name__})")
    return cls()
