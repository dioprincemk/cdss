"""
ai/llm/provider.py
-------------------
LLM provider abstraction for clinical explanations.

DESIGN: The LLMProvider is an abstract base class. All providers implement
the same interface. Switching from OpenAI to a local LLM (e.g., Ollama) or
mock requires only changing the LLM_PROVIDER environment variable.

Providers:
  - MockProvider:   Returns deterministic explanations (default for demos)
  - OpenAIProvider: Calls GPT-4o-mini (requires OPENAI_API_KEY)
  - LocalProvider:  Calls Ollama or any OpenAI-compatible local server
"""
import json
import logging
from abc import ABC, abstractmethod
from typing import Dict, List, Optional

import httpx

from core.config.settings import get_settings

settings = get_settings()
logger = logging.getLogger(__name__)

DISCLAIMER = (
    "\n\n---\n"
    "⚕️ **Clinical Disclaimer**: This analysis is generated by an AI system "
    "to support clinical decision-making. It does not replace professional "
    "medical judgment. All findings must be reviewed and confirmed by a "
    "qualified healthcare professional."
)

CLINICAL_PROMPT_TEMPLATE = """You are a clinical decision support AI assisting a physician with pulmonary disease assessment.

Patient Information:
- Age: {age} years, Sex: {sex}
- Chief Complaint: {chief_complaint}
- Symptoms: {symptoms}
- Symptom Duration: {symptom_duration}
- Existing Conditions: {conditions}
- Current Medications: {medications}

Vital Signs:
- Temperature: {temperature}°C, SpO2: {spo2}%, Respiratory Rate: {rr}/min
- Pulse: {pulse} bpm, BP: {bp_sys}/{bp_dia} mmHg, BMI: {bmi}

AI Model Prediction (DenseNet121 Chest X-Ray Analysis):
- Predicted Diagnosis: {predicted_class}
- Confidence: {top_confidence:.1%}
- All Class Probabilities: {confidence_scores}

Provide a structured clinical explanation with:
1. DIAGNOSTIC EXPLANATION: Interpret the AI findings in context of the patient's presentation
2. SEVERITY ASSESSMENT: Rate severity as Low/Moderate/High/Critical with clinical justification
3. CLINICAL REASONING: Correlate imaging findings with symptoms and vitals
4. RECOMMENDED NEXT STEPS: List 3-5 specific clinical actions (investigations, treatments, referrals)

Keep the response professional, evidence-based, and suitable for a clinical report.
Respond in JSON format:
{{
  "explanation": "...",
  "severity": "Low|Moderate|High|Critical",
  "clinical_reasoning": "...",
  "recommendations": ["...", "...", "..."]
}}"""


class LLMProvider(ABC):
    """Abstract interface for all LLM clinical explanation providers."""

    @abstractmethod
    async def generate_explanation(self, prompt_data: Dict) -> Dict:
        """
        Generate a clinical explanation.

        Args:
            prompt_data: Dict containing patient, vitals, prediction data

        Returns:
            {
                "explanation": str,
                "severity": str,
                "clinical_reasoning": str,
                "recommendations": List[str],
                "provider": str,
                "model_name": str,
                "tokens_used": Optional[int],
                "raw_response": dict,
            }
        """
        ...


